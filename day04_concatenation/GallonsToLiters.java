package day04_concatenation;

public class GallonsToLiters {

    public static void main(String[] args) {

        double gallon = 10,
                liters = 3.79,
                pounds = gallon * liters;


        System.out.println(gallon + " is equal to " + pounds + " pounds.");


    }

}
